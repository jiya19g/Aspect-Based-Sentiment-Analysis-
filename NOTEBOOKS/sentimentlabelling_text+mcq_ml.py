"""# Data Preparation"""

print("\nData Preparation:")

# Method 1: Select columns properly using a list
selected_columns = ['feedback_text', 'aspect', 'sentiment_roberta']
mcq_columns = [col for col in df.columns if col.startswith('MCQ_')]

# Combine all columns
all_columns = selected_columns + mcq_columns
df = df[all_columns]

# Rename sentiment column
df = df.rename(columns={'sentiment_roberta': 'sentiment'})

# Encode sentiment labels
label_map = {
    'Negative': 0,
    'Neutral': 1,
    'Positive': 2
}

df['sentiment'] = df['sentiment'].map(label_map)

# Drop missing values
df = df.dropna().reset_index(drop=True)

# Create text with aspect conditioning
df['text_with_aspect'] = df['feedback_text'] + " [ASPECT: " + df['aspect'] + "]"

print(f"  Final samples: {len(df):,}")
print("\nClass distribution:")
class_dist = df['sentiment'].value_counts(normalize=True)
for label, prop in class_dist.items():
    label_name = list(label_map.keys())[list(label_map.values()).index(label)]
    print(f"  {label_name}: {prop:.3f}")

"""# MCQ Feature Engineering"""

print("\nMCQ Feature Engineering:")

# Define MCQ to aspect mapping (based on course_type)
# Note: Since each feedback has a course_type, we use course_type-specific mapping
# But for simplicity and generalization, we create features from all MCQ columns

mcq_columns = [col for col in df.columns if col.startswith('MCQ_')]
print(f"  MCQ columns found: {mcq_columns}")

mcq_response_map = {
    'Strongly Disagree': 1,
    'Disagree': 2,
    'Neutral': 3,
    'Agree': 4,
    'Strongly Agree': 5
}

for col in mcq_columns:
    df[col + '_num'] = df[col].map(mcq_response_map)

mcq_feature_cols = []

df['mcq_overall_mean'] = df[[col + '_num' for col in mcq_columns]].mean(axis=1)
mcq_feature_cols.append('mcq_overall_mean')

df['mcq_std'] = df[[col + '_num' for col in mcq_columns]].std(axis=1)
mcq_feature_cols.append('mcq_std')

positive_cols = [col + '_num' for col in mcq_columns]
df['mcq_positive_count'] = (df[positive_cols] >= 4).sum(axis=1)
mcq_feature_cols.append('mcq_positive_count')

df['mcq_negative_count'] = (df[positive_cols] <= 2).sum(axis=1)
mcq_feature_cols.append('mcq_negative_count')

df['mcq_neutral_count'] = (df[positive_cols] == 3).sum(axis=1)
mcq_feature_cols.append('mcq_neutral_count')

print(f"  Created {len(mcq_feature_cols)} MCQ features")
print(f"  Features: {mcq_feature_cols}")

"""# Train Val Test Split"""

print("\nData Splitting:")

X_text = df['text_with_aspect'].values
X_mcq = df[mcq_feature_cols].values
y = df['sentiment'].values

X_text_train, X_text_temp, X_mcq_train, X_mcq_temp, y_train, y_temp = train_test_split(
    X_text, X_mcq, y,
    test_size=0.3,
    random_state=42,
    stratify=y
)

X_text_val, X_text_test, X_mcq_val, X_mcq_test, y_val, y_test = train_test_split(
    X_text_temp, X_mcq_temp, y_temp,
    test_size=0.5,
    random_state=42,
    stratify=y_temp
)

print(f"  Train: {len(X_text_train):,}")
print(f"  Validation: {len(X_text_val):,}")
print(f"  Test: {len(X_text_test):,}")

"""# TF-IDF"""

print("\nText Feature Extraction (TF-IDF):")

vectorizer = TfidfVectorizer(max_features=5000)

X_text_train_tfidf = vectorizer.fit_transform(X_text_train)
X_text_val_tfidf = vectorizer.transform(X_text_val)
X_text_test_tfidf = vectorizer.transform(X_text_test)

print(f"  TF-IDF shape: {X_text_train_tfidf.shape}")

"""# MCQ Feature Scaling"""

print("\nMCQ Feature Scaling:")

scaler = StandardScaler()

X_mcq_train_scaled = scaler.fit_transform(X_mcq_train)
X_mcq_val_scaled = scaler.transform(X_mcq_val)
X_mcq_test_scaled = scaler.transform(X_mcq_test)

print(f"  MCQ features shape: {X_mcq_train_scaled.shape}")

"""# Combining Features for ML Models"""

print("\nCombining Features for ML Models:")

X_train_combined = hstack([X_text_train_tfidf, csr_matrix(X_mcq_train_scaled)])
X_val_combined = hstack([X_text_val_tfidf, csr_matrix(X_mcq_val_scaled)])
X_test_combined = hstack([X_text_test_tfidf, csr_matrix(X_mcq_test_scaled)])

print(f"  Combined shape: {X_train_combined.shape}")

"""# Class weights"""

